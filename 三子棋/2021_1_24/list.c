void list()
{
    int y = 1;
    list_choose(y);
    do
    {
        char x = getch();
        if (x == 'w' || x == 'W')
        {
            if (y == 1)
                y = 4;
            else
                y--;
            system("cls");
            list_choose(y);
        }
        else if (x == 's' || x == 'S')
        {
            if (y == 4)
                y = 1;
            else
                y++;
            system("cls");
            list_choose(y);
        }
        else if (getchar() == '\n')
            jump(y);
        else
            continue;
    } while (1);
}